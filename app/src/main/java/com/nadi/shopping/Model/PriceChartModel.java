package com.nadi.shopping.Model;

public class PriceChartModel {

    String id;
    String munth;
    String price;

    public PriceChartModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMunth() {
        return munth;
    }

    public void setMunth(String munth) {
        this.munth = munth;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
