package com.nadi.shopping.Model;

public class IntroductionPagerModel {

  String id;
  String link_animate;
  String description;

    public IntroductionPagerModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLink_animate() {
        return link_animate;
    }

    public void setLink_animate(String link_animate) {
        this.link_animate = link_animate;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
